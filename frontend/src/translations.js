export const t = (key, lang) => {
  if (!lang || !TRANSLATIONS[lang]) return TRANSLATIONS.en[key] || key;
  return TRANSLATIONS[lang][key] || TRANSLATIONS.en[key] || key;
};

export const translatePrediction = (pred, lang) => {
  if (!pred || pred === "—" || pred === "No prediction") return pred;
  
  // Try clean lowercase match
  const lower = pred.toLowerCase().trim();
  const langObj = TRANSLATIONS[lang] || TRANSLATIONS.en;
  
  if (langObj[lower]) {
    const translated = langObj[lower];
    // Match capitalization of input
    if (pred === pred.toUpperCase() && pred.length <= 3) {
      return translated.toUpperCase();
    }
    return translated.charAt(0).toUpperCase() + translated.slice(1);
  }
  
  // Support character mapping if spelling A-Z
  if (pred.length === 1 && /^[A-Z]$/.test(pred)) {
    return pred; // keep character A-Z for spelling
  }
  
  return pred;
};
